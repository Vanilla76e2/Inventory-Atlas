using System.ComponentModel.DataAnnotations;

namespace Inventory_Atlas.Core.Enums
{
    /// <summary>
    /// Статус документа.
    /// <para/>
    /// Используется для отслеживания состояния документов (например, при выдаче, возврате или списании имущества).
    /// </summary>
    public enum DocumentStatus
    {
        /// <summary>
        /// Черновик — документ создан, но ещё не заполнен полностью или не готов к обработке.
        /// </summary>
        [Display(Name = "Черновик")]
        Draft = 0,

        /// <summary>
        /// Ожидает подтверждения — документ готов к проверке и ожидает согласования или утверждения.
        /// </summary>
        [Display(Name = "Ожидает подтверждения")]
        Waiting = 1,

        /// <summary>
        /// Проведён — документ подтверждён и проведён в системе, данные актуальны.
        /// </summary>
        [Display(Name = "Проведён")]
        Approved = 2,

        /// <summary>
        /// Отклонён — документ был рассмотрен и отклонён, не проведён в системе.
        /// </summary>
        [Display(Name = "Отклонён")]
        Rejected = 3,

        /// <summary>
        /// Отменён — документ был создан, но позже отменён пользователем или администратором.
        /// </summary>
        [Display(Name = "Отменён")]
        Cancelled = 4
    }
}
